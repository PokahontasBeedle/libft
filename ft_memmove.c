#include "libft.h"

void	*ft_memmove(void *destination, const void *source, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)destination;
	s = (unsigned char *)source;
	if (d == s || len == 0)
		return (d);
	if (d < s)
		while (len--)
			*d++ = *s++;
	else
	{
		d += len;
		s += len;
		while (len--)
			*--d = *--s;
	}
	return (destination);
}
